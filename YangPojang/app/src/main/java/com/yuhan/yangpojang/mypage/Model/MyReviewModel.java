package com.yuhan.yangpojang.mypage.Model;

public class MyReviewModel {
    private String shopName;
    private String shopAdderss;
    private String category;
    private String summary;
    private String pic1;
    private int rating;

    public String getShopName() {    return shopName;    }

    public void setShopName(String shopName) {    this.shopName = shopName;    }

    public String getShopAdderss() {    return shopAdderss;    }

    public void setShopAdderss(String shopAdderss) {   this.shopAdderss = shopAdderss;    }

    public String getCategory() {      return category;    }

    public void setCategory(String category) {   this.category = category;    }

    public String getSummary() {     return summary;    }

    public void setSummary(String summary) {     this.summary = summary;    }

    public String getPic1() {
        return pic1;
    }

    public void setPic1(String pic1) {
        this.pic1 = pic1;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }


}

